    public void letras(JTextField a) {
        a.addKeyListener(new KeyAdapter() {
            public void keyTyped(KeyEvent e) {
                char c = e.getKeyChar();
                if (Character.isDigit(c)) {
                    //getToolkit().beep();
                    e.consume();
                }
            }
        });
    }

    private int ct = 10;

