    private void BotonIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonIngresarActionPerformed
        // TODO add your handling code here:

        ConexionSistemaBancario cn = new ConexionSistemaBancario().conectar();            // Conecta con la base de datos XE con el usuario system y la contraseña password
        Connection c = cn.getConexion();

        try {
            CallableStatement cst = c.prepareCall("{call PA_INSERTAR_CLIENTE (?,?,?,?,?,?,?)}");
            cst.setInt(1, Integer.parseInt(cedula_cliente.getText()));
            cst.setString(2, ruc.getText());
            cst.setString(3, nombre_cliente.getText());
            cst.setString(4, situacion_laboral.getText());
            cst.setString(5, direccion_cliente.getText());
            cst.setString(6, correo_cliente.getText());
            cst.setInt(7, Integer.parseInt(telefono_cliente.getText()));
            cst.execute();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Error: " + ex.getMessage());
        } finally {
            try {
                c.close();
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, "Error: " + ex.getMessage());
            }
        }

        cedula_cliente.setText("");
        ruc.setText("");
        nombre_cliente.setText("");
        situacion_laboral.setText("");
        direccion_cliente.setText("");
        correo_cliente.setText("");
        telefono_cliente.setText("");

        //JOptionPane.showMessageDialog(null, "La información se guardo con éxito!");                       

    }//GEN-LAST:event_BotonIngresarActionPerformed

    private void BotonBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonBuscarActionPerformed
        // TODO add your handling code here:
        ConexionSistemaBancario f = new ConexionSistemaBancario();
        f.conectar();

        int id = Integer.parseInt(cedula_cliente.getText());
        ResultSet resultado;

        try {
            resultado = f.consultar("SELECT ruc, nombre_cliente, situacion_laboral, direccion_cliente, correo_cliente, telefono_cliente FROM cliente "
                    + "WHERE cedula_cliente ='" + id + "'");
            while (resultado.next()) {
                String Rc = resultado.getString("ruc");
                String nombrecliente = resultado.getString("nombre_cliente");
                String situacionlaboral = resultado.getString("situacion_laboral");
                String direccioncliente = resultado.getString("direccion_cliente");
                String correocliente = resultado.getString("correo_cliente");
                int telefonocliente = resultado.getInt("telefono_cliente");

                ruc.setText("" + Rc);
                nombre_cliente.setText("" + nombrecliente);
                situacion_laboral.setText("" + situacionlaboral);
                direccion_cliente.setText("" + direccioncliente);
                correo_cliente.setText("" + correocliente);
                telefono_cliente.setText("" + telefonocliente);
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Error de conexion con la base de datos");
        }
    }//GEN-LAST:event_BotonBuscarActionPerformed

